 <script>
     document.addEventListener("DOMContentLoaded", function() {
         const sliderTrack = document.querySelector(".slider-track");
         let items = document.querySelectorAll(".slider-item");

         // Clone items for infinite loop effect
         items.forEach((item) => {
             const clone = item.cloneNode(true);
             sliderTrack.appendChild(clone);
         });

         let scrollAmount = 0;

         function animateSlider() {
             scrollAmount += 1; // speed of the slider
             sliderTrack.style.transform = `translateX(-${scrollAmount}px)`;

             // Reset the scroll position to create an infinite loop effect
             if (scrollAmount >= sliderTrack.scrollWidth / 2) {
                 scrollAmount = 0;
             }

             requestAnimationFrame(animateSlider);
         }

         animateSlider();
     });
 </script>
